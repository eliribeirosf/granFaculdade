// Trabalhando com estruturas condicionais (if, if-else e else)
let media = 7;
if (media >= 7){
    console.log("Aprovado!");
}
else if(media>=5 && media<7){
    console.log("Recuperação!");
}
else{
    console.log("Reprovado!");
}

// Trabalhando com operador ternário
let mediaTernario = 5;
mediaTernario>=7 ? console.log("Aprovado!") : console.log("Reprovado!")